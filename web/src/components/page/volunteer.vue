<script>
    import addPeople from "../common/addPeople";
    import axios from 'axios';
    import Cookies from 'js-cookie'
    export default {
        name: 'tabs',
        components:{addPeople},
        data() {
            return {
                deleteVisible:false,
                message: 'first',
                showHeader: false,
                listForm1:{},
                addVisible:false,
                isEdit:true,
                tableIndex:'',
                tableData: [
                ],
                // id:Cookies.get('userid'),
                keyWord:'',
                idSet:[],
                id:'',
            }
        },
        mounted() {
         this.getTableData()
        },
        methods: {
            getTableData(){
                axios.get(axios.defaults.baseURL+'select_Volunteer',{params:{

                    }}).then(res=>{
                    // if(res.data.error=='1'){
                    //     this.$message.error(res.data.messages)
                    //     return
                    // }
                    this.tableData=res.data.data
                })
            },
            // querySearch(queryString, cb) {
            //     axios.get(axios.defaults.baseURL+'select_Volunteer')
            // },
            updateNewVolunteer(){
                // insert_Employee
                // update_Employee
                let url='insert_Volunteer'
                let data=this.$refs.addPeople.VolunteerForm
                if(this.isEdit){
                    url='update_Volunteer'
                    data={
                        'id':parseInt(data.id),
                        update:data,
                    }
                }

                axios.get(axios.defaults.baseURL+url,{params:
                    data
                }).then(res=>{
                    if(res.data.error!=0){
                        this.$message.error(res.data.messages)
                        this.addVisible=false
                        return
                    }
                    this.addVisible=false
                    this.$message.success('操作成功')
                    this.getTableData()
                })
            },
            handleDelete(index, row) {
                this.id=this.tableData[index].id
                this.deleteVisible=true
            },
            deleteNewWorker(idSet){
                let data={id:this.id}
                if(idSet)
                    data.id=idSet
                axios.get(axios.defaults.baseURL+'delete_Volunteer',{params:
                    data
                }).then(res=>{
                    if(res.data.error!=0){
                        this.$message.error(res.data.messages)
                        this.deleteVisible=false
                        return
                    }
                    this.deleteVisible=false
                    this.$message.success('操作成功')
                    this.getTableData()
                })
            },
            handleEdit(index, row){
                this.isEdit=true
                this.tableIndex=index;
                this.addVisible=true;
            },
            handleEditDialog(){
                if(!this.isEdit){
                    this.$refs.addPeople.VolunteerForm={
                        id:'',
                        username:'',
                        gender:'',
                        phone:'',
                        id_card:'',
                        birthday:'',
                        checkin_date:'',
                        checkout_date:'',
                        imgset_dir:'',
                        profile_photo:'',
                    }
                    return
                }
                this.$refs.addPeople.VolunteerForm=this.tableData[this.tableIndex]
            },
            handleSelectionChange(val){
                this.idSet=[]
                val.forEach(item=>{
                    this.idSet.push(item.id)
                })
            },
            delAllSelection(){
                this.idSet.forEach(item=>{
                    this.deleteNewWorker(item)
                })
            }
        },
    }

</script>

