            </div>
            <span slot="footer" class="dialog-footer">
                <el-button @click="updateNewVolunteer()" type="primary">确 定</el-button>
                <el-button @click="addVisible = false">取 消</el-button>
              </span>
        </el-dialog>
        <el-dialog
                :title="'确定要删除'"
                :visible.sync="deleteVisible"
                width="500px"
                cente
